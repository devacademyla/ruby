# Es igual a
puts true == true
# => true

# Es diferente de
puts 10 != 'hola'
# => true

# Es mayor que
puts 5 > 9
# => false

# Es menor que
puts 6 < 3
# => false

# Mayor o igual
puts 10 >= 8
# => false

# Menor o igual
puts 15 <= 16
# => true

# Operador and (&&)
puts true && false
# => false

# Operador or (||)
puts true || false
# => true

# Operador not
puts not(true)
# => false
puts not(false)
# => true
